package org.sagebionetworks.repo.model;

/**
 * Used to pass a file url to a web service.
 * @author John
 *
 */
public class RestoreFile {
	
	private String url = null;
	
	public RestoreFile() {
	}

	public RestoreFile(String url) {
		this.url = url;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((url == null) ? 0 : url.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		RestoreFile other = (RestoreFile) obj;
		if (url == null) {
			if (other.url != null)
				return false;
		} else if (!url.equals(other.url))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "RestoreFile [url=" + url + "]";
	}

}
